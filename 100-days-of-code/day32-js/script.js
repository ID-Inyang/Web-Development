const grade = 'p';

switch (grade) {
    case 'A':
        console.log('Excellent work!');
        break;
    case 'B':
        console.log('Good job!');
        break;
    case 'C':
        console.log('You passed!');
        break;
    case 'D':
        console.log('You need to improve!');
        break;
    default:
        console.log('Invalid grade!');
}